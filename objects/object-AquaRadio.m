            if (!rgb[4+j+2] && (rgb[4+j+1] == 255) && !rgb[4+j+0]) {
                continue;
            }
            pixelBytes[i] = rgb[4+j+2];
            pixelBytes[i+1] = rgb[4+j+1];
            pixelBytes[i+2] = rgb[4+j+0];
            pixelBytes[i+3] = 255;
        }
    }
}
