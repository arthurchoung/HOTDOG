#import "HOTDOG.h"

#include <sys/time.h>

@implementation Definitions(mfkelwmfklsdmklfm)
+ (id)gettimeofday
{
    struct timeval tv;
    gettimeofday(&tv, NULL);
    return nsfmt(@"%ld.%06ld", tv.tv_sec, tv.tv_usec);
}
@end

