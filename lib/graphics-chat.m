+ (Int4)drawChatBubbleInBitmap:(id)bitmap rect:(Int4)r text:(id)text
{
    Int4 chatRect = [Definitions drawChatBubbleInBitmap:bitmap rect:r text:text fgcolor:@"#000000" bgcolor:@"#ffffff" flipHorizontal:NO flipVertical:NO];
